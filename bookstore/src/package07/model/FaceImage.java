package package07.model;

public class FaceImage {
	private Integer id;		//ID编号
	private String face;	//头像地址
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getFace() {
		return face;
	}
	public void setFace(String img) {
		this.face = img;
	}
}
